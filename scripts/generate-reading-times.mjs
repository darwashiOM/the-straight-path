#!/usr/bin/env node
/**
 * generate-reading-times.mjs
 *
 * Pre-computes reading time for every MDX article from the raw source and
 * writes a generated TS map, so `articles/index.ts` can consume it at build
 * time without a runtime `?raw` glob (the MDX plugin intercepts `?raw`
 * queries, which would otherwise make reading time show as 1 min for all
 * articles).
 *
 * Runs as part of `prebuild`.
 */
import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const ARTICLES_DIR = join(ROOT, 'apps', 'web', 'src', 'content', 'articles');
const OUT_PATH = join(ARTICLES_DIR, 'reading-times.generated.ts');

const WORDS_PER_MINUTE = 220;

function wordCount(raw) {
  const body = raw.replace(/^---[\s\S]*?---\s*/, '');
  const cleaned = body
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`[^`]*`/g, ' ')
    .replace(/!\[[^\]]*\]\([^)]*\)/g, ' ')
    .replace(/\[[^\]]*\]\([^)]*\)/g, ' ')
    .replace(/[#>*_~\-]/g, ' ');
  return cleaned.trim().split(/\s+/).filter(Boolean).length;
}

function readingTimeMinutes(raw) {
  return Math.max(1, Math.round(wordCount(raw) / WORDS_PER_MINUTE));
}

function slugFromFrontmatter(raw) {
  const match = raw.match(/^---\n([\s\S]*?)\n---/);
  if (!match) return null;
  const slugLine = match[1].split('\n').find((l) => l.startsWith('slug:'));
  if (!slugLine) return null;
  return slugLine.slice(5).trim().replace(/^['"]|['"]$/g, '');
}

function slugFromFilename(file) {
  return file.replace(/\.mdx$/, '');
}

function stripMdx(raw) {
  const withoutFm = raw.replace(/^---[\s\S]*?---\n?/, '');
  return withoutFm
    .replace(/```[\s\S]*?```/g, ' ')
    .replace(/`[^`]*`/g, ' ')
    .replace(/!\[[^\]]*]\([^)]*\)/g, ' ')
    .replace(/\[([^\]]+)]\([^)]*\)/g, '$1')
    .replace(/<[^>]+>/g, ' ')
    .replace(/[#>*_~]+/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

const readingTimes = {};
const bodySnippets = {};
for (const file of readdirSync(ARTICLES_DIR)) {
  if (!file.endsWith('.mdx')) continue;
  const raw = readFileSync(join(ARTICLES_DIR, file), 'utf8');
  const slug = slugFromFrontmatter(raw) || slugFromFilename(file);
  readingTimes[slug] = readingTimeMinutes(raw);
  bodySnippets[slug] = stripMdx(raw).slice(0, 500);
}

const body = `// Auto-generated by scripts/generate-reading-times.mjs. Do not edit.
export const readingTimes: Record<string, number> = ${JSON.stringify(readingTimes, null, 2)};

export const bodySnippets: Record<string, string> = ${JSON.stringify(bodySnippets, null, 2)};
`;

writeFileSync(OUT_PATH, body);
console.log(`Wrote ${OUT_PATH} (${Object.keys(readingTimes).length} articles)`);
